CREATE TABLE Students (
  LastName   NVARCHAR(64) NOT NULL,
  FirstName  NVARCHAR(64) NOT NULL,
  NetID      NVARCHAR(64) PRIMARY KEY NOT NULL
);

CREATE TABLE Courses (
  CRN     		INT PRIMARY KEY NOT NULL,
  Department   	NVARCHAR(64) NOT NULL,
  CourseNum     INT NOT NULL,
  Semester   	NVARCHAR(64) NOT NULL,
  Year    		SMALLINT NOT NULL,
  CourseType   	NVARCHAR(64) NOT NULL,
  Day		   	NVARCHAR(64) NOT NULL,
  ClassTime  	NVARCHAR(64) NOT NULL, 
  ClassSize    	SMALLINT NOT NULL,
  NumEnrolled	SMALLINT NOT NULL
);

CREATE TABLE StudentCourses (
  SCN 			INT IDENTITY(1001,1) PRIMARY KEY,
  NetID 		NVARCHAR(64) NOT NULL FOREIGN KEY REFERENCES Students(NetID),
  CRN 			INT NOT NULL FOREIGN KEY REFERENCES Courses(CRN)
);

CREATE INDEX SCourses_NetID_Index
ON StudentCourses(NetID);

CREATE INDEX SCourses_CRN_Index
ON StudentCourses(CRN);

CREATE TABLE Waitlist(
  WLN 			INT IDENTITY(20001, 1) PRIMARY KEY,
  NetID 		NVARCHAR(64) NOT NULL FOREIGN KEY REFERENCES Students(NetID),
  CRN 			INT NOT NULL FOREIGN KEY REFERENCES Courses(CRN)
);

CREATE INDEX Waitlist_NetID_Index
ON Waitlist(NetID);

CREATE INDEX Waitlist_CRN_Index
ON Waitlist(CRN);